    public void loadTableFares(){
        
        String selectedBaiXe = jComboBox1.getSelectedItem().toString();
     if(selectedBaiXe.equalsIgnoreCase("tất cả")) {
     admin.parking_id = "all";
     admin.parking_address = "no_address";
     admin.parking_name = "no_name";
     } else {
        admin.parking_id = selectedBaiXe;
        if(db.selectQuick("name,address", "parking_info", "id = " + selectedBaiXe)){
        admin.parking_name = db.kq.get(0).get(0);
        admin.parking_address = db.kq.get(0).get(1);
        }
        }
        loadTable(TableGiaVe, this.admin.getFareHistory());
     
    
    }
    private void capNhatGiaVe() {
        String typeVehicle = (String) jComboBox_LoaiXe.getSelectedItem();
        String giaVe = jTextField_GiaVe.getText();
        if(!checkDouble(giaVe)) JOptionPane.showMessageDialog(null, "Kiểm tra lại giá vé");
        else  {
        if(this.admin.updateFares(typeVehicle,giaVe )) {JOptionPane.showMessageDialog(null, "Cập nhật giá vé thành công");this.loadTableFares();}
        else JOptionPane.showMessageDialog(null, "Cập nhật giá vé thất bại");
        }
    }
