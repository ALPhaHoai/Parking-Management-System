void thongKe(){
        try {
            String timeIn = dateChooserComboFrom.getText();
            String timeOut = dateChooserComboTo.getText();
            DateFormat df = new SimpleDateFormat("MM/dd/yy");
            if(timeIn != null && timeIn.length() > 0 && timeIn.contains("/")){
            Date from = df.parse(timeIn);
            timeIn = new SimpleDateFormat("yyyy/MM/dd").format(from);
            } else timeIn = null;
            if(timeOut != null && timeOut.length() > 0 && timeOut.contains("/")){
            Date to = df.parse(timeOut);
            timeOut = new SimpleDateFormat("yyyy/MM/dd").format(to);
            } else timeOut = null;
            
            String trangThai = ComboBox_TrangThai.getSelectedItem().toString();
            String loaiXe = ComboBox_LoaiXe.getSelectedItem().toString();
            String loaiThongKe = jComboBox_LoaiThongKe.getSelectedItem().toString();
            String ThoiGianVaoRa = ComboBox_ThoiGianVaoRa.getSelectedItem().toString();
            
            int tongTien = 0;
// Reset lại bảng
     DefaultTableModel dm = (DefaultTableModel) ThongKeTable.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        
    //Load bảng thống kê vào từ DB
        ArrayList<ArrayList<String>> thongKe = this.manager.getThongKe(timeIn, timeOut,loaiXe, trangThai,ThoiGianVaoRa, loaiThongKe);
        if(thongKe != null && thongKe.size() > 0) {
            for (int i = 0; i < thongKe.size(); i++) {
                ArrayList<String> thisThongKe = thongKe.get(i);
                thisThongKe.add(0, String.valueOf(i + 1));
                if(checkNumeric(thisThongKe.get(2))) tongTien += Integer.valueOf(thisThongKe.get(2));
                String[] rowData = thisThongKe.toArray(new String[thisThongKe.size()]);
               dm.addRow(rowData);
            }    
           
        //Tính tổng số tiền
        Label_TongTien.setText("Tổng số tiền: " +String.valueOf(tongTien) + " VNĐ");
        //Luot giu xe
        Label_LuotGui.setText(String.valueOf(thongKe.size()) + " lượt gửi");
} else {
        Label_TongTien.setText("");
        Label_LuotGui.setText("");
        }
        
        
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Đã có lỗi xảy ra");
        }
}
