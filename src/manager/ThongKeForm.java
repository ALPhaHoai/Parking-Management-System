   XSSFSheet ws = wb.createSheet();
   
   //Load data to Treemap
   TreeMap<String,Object[]> data = new TreeMap();
   data.put("0", new Object[]{dm.getColumnName(0),dm.getColumnName(1),dm.getColumnName(2),dm.getColumnName(3)});
        for (int i = 0; i < dm.getRowCount(); i++) {
            data.put(String.valueOf(i+1), new Object[]{
                getCellVal(dm,i,0),
                getCellVal(dm,i,1),
                getCellVal(dm,i,2),
                getCellVal(dm,i,3),
                });
        }
   //Write to sheet
   Set<String> ids = data.keySet();
   XSSFRow row ;
   int rowID = 0;
   
   for(String key:ids){
   row = ws.createRow(rowID++);
   //get data as per key
   Object[] values = data.get(key);
   int cellID = 0;
   for(Object o : values){
   XSSFCell cell = row.createCell(cellID++);
   if(o != null) cell.setCellValue(String.valueOf(o));
   else cell.setCellValue("");
   }
   }
   
   //Write excell to file system
   xuatFileExcel(fileName,wb);
   
